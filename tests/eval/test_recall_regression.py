"""Gate-wired recall@K + MRR regression test over a fixed fixture repository.

TDD: this test is written to define the contract; golden.json was generated by
running the live index against the fixture and capturing the expected outputs.

Coverage:
  SHA-STAMP:   fail if fixture has been mutated without regenerating golden
  RECALL:      recall@K >= golden baseline (with tolerance) for each query
  MRR:         MRR >= golden baseline (with tolerance) for each query
  AGGREGATE:   mean recall@K and MRR >= baseline
  SYNTHESIS:   synthesis-sensitive queries (interface-override, event-emitter)
               explicitly included in golden so synthesis delta is measurable

Usage in gate:
  make gate → make test → pytest tests/  — picks up this file automatically.

Usage standalone (prints metrics):
  make eval

Design decisions:
  - Indexes the fixture via the same code path as 'seam init' (walk_project +
    index_one_file + index_clusters + index_synthesis) — tests the full pipeline.
  - Uses a tempfile DB so tests are fully isolated and don't pollute the repo.
  - Queries run through the EXISTING transport-agnostic handlers in seam/server/tools.py —
    no new MCP tools, no new query logic. Same as the real MCP tools.
  - Tolerance of 0.0 on exact recall (the golden IS the minimum bar — no regression
    means reaching the same numbers). Because the fixture is small and deterministic,
    a 0-tolerance bar is correct: a score lower than golden means a regression.
  - Synthesis is ON (default). synthesis=off would lower synthesis-sensitive queries to
    demonstrate the delta — but we do NOT test synthesis=off here (that's covered by the
    existing synthesis integration tests). This test is a REGRESSION guard, not a diff test.
"""

from __future__ import annotations

import json
import tempfile
from pathlib import Path
from typing import Any

import pytest

from tests.eval.recall_harness import (
    check_fixture_hash,
    compute_metrics,
    mrr,
    recall_at_k,
)

# ── Constants ─────────────────────────────────────────────────────────────────

FIXTURE_DIR = Path(__file__).parent / "fixtures"
GOLDEN_PATH = Path(__file__).parent / "golden.json"

# Recall regression tolerance:
#   0.0 → NO regression allowed (the golden IS the minimum bar).
# A nonzero tolerance (e.g. 0.05 = 5%) would allow minor fluctuations from
# upstream engine changes. We use 0.0 for strictness on this small, deterministic fixture.
_RECALL_TOLERANCE = 0.0

# ── Fixture: index the eval fixture repo ─────────────────────────────────────


@pytest.fixture(scope="module")
def indexed_conn() -> Any:
    """Index the eval fixture repo and return an open DB connection.

    scope="module" so the (slow) indexing is done ONCE per test module run.
    The temp directory is created via TemporaryDirectory (kept open for module lifetime).
    """
    import seam.config as cfg
    from seam.indexer.cluster_index import index_clusters
    from seam.indexer.db import init_db
    from seam.indexer.pipeline import index_one_file, walk_project
    from seam.indexer.synthesis_index import index_synthesis

    # Use a real temp directory on disk (not :memory:) because the synthesis bridge
    # needs to read source files from disk via _load_file_sources.
    # The temp directory persists for the module scope, then auto-deletes.
    tmp = tempfile.mkdtemp(prefix="seam_eval_")
    tmp_path = Path(tmp)
    db_path = tmp_path / "seam.db"

    conn = init_db(db_path)

    # Index every file in the fixture directory (same code path as `seam init`).
    files = walk_project(FIXTURE_DIR)
    assert files, f"No files found in fixture dir: {FIXTURE_DIR}"
    for fpath in files:
        index_one_file(conn, fpath)

    # Run clustering (required for cluster_id fields) and synthesis (A1 + A2 channels).
    index_clusters(
        conn,
        naming_mode="deterministic",
        llm_api_key=None,
        llm_model=None,
        min_size=2,
    )
    index_synthesis(
        conn,
        enabled=True,
        fanout_cap=cfg.SEAM_SYNTHESIS_FANOUT_CAP,
    )

    return conn


@pytest.fixture(scope="module")
def golden() -> dict[str, Any]:
    """Load the golden.json file. Fails if the file does not exist."""
    assert GOLDEN_PATH.exists(), (
        f"Golden file missing: {GOLDEN_PATH}. Run 'make eval' or the gen script to regenerate."
    )
    return json.loads(GOLDEN_PATH.read_text(encoding="utf-8"))


# ── SHA-stamp test ────────────────────────────────────────────────────────────


def test_fixture_hash_matches_golden(golden: dict[str, Any]) -> None:
    """Fail if the fixture has been mutated without regenerating the golden.

    This test exists to detect silent golden drift: if someone changes a fixture
    file without updating golden.json, the golden's expected_symbols may no longer
    reflect the current fixture content and the regression test would silently
    trust stale expectations. The SHA-stamp makes this visible as a test failure.

    To regenerate the golden: run 'make eval-generate' or the gen script.
    """
    matches, stored_hash, current_hash = check_fixture_hash(GOLDEN_PATH, FIXTURE_DIR)

    if not stored_hash:
        # No hash in golden — the check is skipped (backward compat).
        pytest.skip("golden.json has no fixture_hash — hash check skipped")

    assert matches, (
        f"Fixture content has changed since the golden was generated.\n"
        f"  stored hash: {stored_hash}\n"
        f"  current hash: {current_hash}\n"
        f"Regenerate golden.json by running: make eval-generate"
    )


# ── Per-query runner ──────────────────────────────────────────────────────────


def _run_query(
    conn: Any,
    query_spec: dict[str, Any],
) -> list[str]:
    """Run a single golden query and return the ranked list of returned symbol names.

    Dispatches based on query_spec['tool']:
      search               → handle_seam_search → symbol names from result list
      query                → handle_seam_query  → symbol names from result list
      context_callers      → handle_seam_context → callers list
      context_field_readers → handle_seam_context → field_readers list (A3)
      context_field_writers → handle_seam_context → field_writers list (A3)
      impact_downstream    → handle_seam_impact(direction='downstream') → all tier names
      impact_upstream      → handle_seam_impact(direction='upstream') → all tier names (A3)
      trace                → handle_seam_trace → callees of source

    Never raises — returns [] on any error so a broken handler doesn't mask other tests.
    """
    from seam.server.tools import (
        handle_seam_context,
        handle_seam_impact,
        handle_seam_query,
        handle_seam_search,
        handle_seam_trace,
    )

    tool = query_spec["tool"]
    try:
        if tool == "search":
            result = handle_seam_search(conn, query_spec["query"], FIXTURE_DIR, semantic=False)
            return [r["symbol"] for r in result] if isinstance(result, list) else []

        elif tool == "query":
            result = handle_seam_query(conn, query_spec["query"], FIXTURE_DIR, semantic=False)
            return [r["symbol"] for r in result] if isinstance(result, list) else []

        elif tool == "context_callers":
            ctx = handle_seam_context(conn, query_spec["symbol"], FIXTURE_DIR)
            if ctx is None or "error" in ctx:
                return []
            # callers are in order — preserves the rank order from the engine.
            return ctx.get("callers", [])

        elif tool == "context_field_readers":
            ctx = handle_seam_context(conn, query_spec["symbol"], FIXTURE_DIR)
            if ctx is None or "error" in ctx:
                return []
            return ctx.get("field_readers", [])

        elif tool == "context_field_writers":
            ctx = handle_seam_context(conn, query_spec["symbol"], FIXTURE_DIR)
            if ctx is None or "error" in ctx:
                return []
            return ctx.get("field_writers", [])

        elif tool == "impact_downstream":
            impact = handle_seam_impact(
                conn,
                query_spec["symbol"],
                FIXTURE_DIR,
                direction="downstream",
                max_depth=3,
                include_tests=True,
                limit=0,  # unlimited — measure full coverage
            )
            # Collect all names from all downstream tiers, in tier order
            # (WILL_BREAK first, then LIKELY_AFFECTED, then MAY_NEED_TESTING).
            names: list[str] = []
            for tier_entries in impact.get("downstream", {}).values():
                names.extend(e["name"] for e in tier_entries)
            return names

        elif tool == "impact_upstream":
            impact = handle_seam_impact(
                conn,
                query_spec["symbol"],
                FIXTURE_DIR,
                direction="upstream",
                max_depth=3,
                include_tests=True,
                limit=0,  # unlimited — measure full coverage
            )
            names: list[str] = []
            for tier_entries in impact.get("upstream", {}).values():
                names.extend(e["name"] for e in tier_entries)
            return names

        elif tool == "trace":
            trace = handle_seam_trace(conn, query_spec["source"], query_spec["target"], FIXTURE_DIR)
            # For trace, the expected_symbols are the callees of the source.
            # We check that the trace is found (path exists) and the target appears in callees.
            callees = [x["name"] for x in trace.get("callees_source", [])]
            return callees

        else:
            return []

    except Exception:  # noqa: BLE001
        # Never let a handler error mask other query results.
        return []


# ── Per-query regression tests ────────────────────────────────────────────────


class TestPerQueryRegression:
    """Per-query recall@K and MRR assertions against the golden baseline.

    Each test loads the corresponding golden query by id and runs the handler,
    then asserts recall@K == golden_recall and MRR == golden_mrr.

    Because the fixture is small and fully deterministic, we use zero tolerance:
    any change that lowers a metric is a regression.
    """

    def _assert_query(
        self,
        conn: Any,
        golden: dict[str, Any],
        query_id: str,
    ) -> None:
        """Helper: run the query and assert no regression vs golden."""
        # Find the golden entry for this query_id
        golden_query = next(
            (q for q in golden.get("queries", []) if q["id"] == query_id),
            None,
        )
        assert golden_query is not None, f"Query id '{query_id}' not found in golden"

        actual = _run_query(conn, golden_query)
        expected = golden_query["expected_symbols"]
        k = golden_query.get("k", 10)

        actual_recall = recall_at_k(expected, actual, k)
        actual_mrr = mrr(expected, actual)

        # Compute the GOLDEN baseline by running metrics against the EXPECTED output.
        # Since we generated the golden by running the live index, the expected symbols
        # ARE the symbols the index returned — so golden recall = 1.0 for all queries
        # (the golden records what the index can find with synthesis ON).
        golden_recall = 1.0  # by construction: every expected symbol was present in live run
        golden_mrr_val = 1.0 if expected else 0.0  # first expected was in top results

        note = golden_query.get("note", query_id)
        assert actual_recall >= golden_recall - _RECALL_TOLERANCE, (
            f"Recall@{k} REGRESSION on '{query_id}' ({note}):\n"
            f"  expected recall: {golden_recall:.4f}, got: {actual_recall:.4f}\n"
            f"  expected symbols: {expected}\n"
            f"  actual symbols (top {k}): {actual[:k]}"
        )
        # MRR tolerance: same as recall
        assert actual_mrr >= golden_mrr_val - _RECALL_TOLERANCE, (
            f"MRR REGRESSION on '{query_id}' ({note}):\n"
            f"  expected MRR: {golden_mrr_val:.4f}, got: {actual_mrr:.4f}\n"
            f"  expected symbols: {expected}\n"
            f"  actual symbols: {actual[:k]}"
        )

    def test_search_validate_data(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """Locate validate_data by text search."""
        self._assert_query(indexed_conn, golden, "search-validate-data")

    def test_search_payment_processor(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """Locate PaymentProcessor and implementations by search."""
        self._assert_query(indexed_conn, golden, "search-payment-processor")

    def test_query_pipeline_stage(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """Locate DataPipeline by concept query."""
        self._assert_query(indexed_conn, golden, "query-pipeline-stage")

    def test_query_event_emitter(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """Locate EventBus by concept query."""
        self._assert_query(indexed_conn, golden, "query-event-emitter")

    def test_context_callers_format_result(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """Callers of format_result via direct call edges."""
        self._assert_query(indexed_conn, golden, "context-callers-format-result")

    def test_impact_downstream_event_emitter_synthesis(
        self, indexed_conn: Any, golden: dict[str, Any]
    ) -> None:
        """Synthesized EE edge: data_received event fires on_data_received_handler."""
        self._assert_query(indexed_conn, golden, "impact-downstream-event-emitter-synthesis")

    def test_impact_downstream_interface_override_synthesis(
        self, indexed_conn: Any, golden: dict[str, Any]
    ) -> None:
        """Synthesized interface-override: PaymentProcessor.process_payment impls."""
        self._assert_query(indexed_conn, golden, "impact-downstream-interface-override-synthesis")

    def test_trace_render_to_format(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """Trace: render_output calls format_result (direct call path)."""
        self._assert_query(indexed_conn, golden, "trace-render-to-format")

    def test_context_callers_validate_data(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """Callers of validate_data: EventBus.process_data calls it."""
        self._assert_query(indexed_conn, golden, "context-callers-validate-data")

    def test_impact_downstream_interface_refund_synthesis(
        self, indexed_conn: Any, golden: dict[str, Any]
    ) -> None:
        """Synthesized interface-override: PaymentProcessor.refund has 2 impls."""
        self._assert_query(indexed_conn, golden, "impact-downstream-interface-refund-synthesis")

    def test_context_field_readers_stages(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """A3: field_readers of DataPipeline.stages (who reads the stages field)."""
        self._assert_query(indexed_conn, golden, "context-field-readers-stages")

    def test_context_field_writers_stages(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """A3: field_writers of DataPipeline.stages (who writes the stages field)."""
        self._assert_query(indexed_conn, golden, "context-field-writers-stages")

    def test_context_field_writers_processor(
        self, indexed_conn: Any, golden: dict[str, Any]
    ) -> None:
        """A3: field_writers of OrderService.processor (dependency injection site)."""
        self._assert_query(indexed_conn, golden, "context-field-writers-processor")

    def test_impact_upstream_stages_field(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """A3: upstream impact of DataPipeline.stages as a field seed."""
        self._assert_query(indexed_conn, golden, "impact-upstream-stages-field")


# ── A3 field-access dedicated test class ──────────────────────────────────────


class TestFieldAccessQueries:
    """Gate-wired check that field-access queries in the golden meet recall 1.0.

    A separate class (not sub-classed from TestPerQueryRegression) so the A3 delta
    is visually grouped and filterable with -k TestFieldAccessQueries.
    """

    _FIELD_ACCESS_IDS = {
        "context-field-readers-stages",
        "context-field-writers-stages",
        "context-field-writers-processor",
        "impact-upstream-stages-field",
    }

    def test_field_access_queries_all_recall_1(
        self, indexed_conn: Any, golden: dict[str, Any]
    ) -> None:
        """All A3 field-access queries must achieve recall@10 = 1.0."""
        for query_spec in golden.get("queries", []):
            if query_spec["id"] not in self._FIELD_ACCESS_IDS:
                continue

            actual = _run_query(indexed_conn, query_spec)
            expected = query_spec["expected_symbols"]
            k = query_spec.get("k", 10)

            actual_recall = recall_at_k(expected, actual, k)
            assert actual_recall >= 1.0 - _RECALL_TOLERANCE, (
                f"Field-access query '{query_spec['id']}' REGRESSION:\n"
                f"  recall@{k} = {actual_recall:.4f} (expected >= 1.0)\n"
                f"  expected: {expected}\n"
                f"  actual: {actual[:k]}\n"
                f"  HINT: A3 field-access edges may be missing. "
                f"Was SEAM_FIELD_ACCESS_EDGES disabled or seam init not re-run?"
            )


# ── Aggregate metric test ─────────────────────────────────────────────────────


class TestAggregateMetrics:
    """Aggregate recall@K and MRR over all golden queries must meet the baseline."""

    def test_aggregate_recall_and_mrr(self, indexed_conn: Any, golden: dict[str, Any]) -> None:
        """All queries together: mean recall@K and MRR must equal 1.0.

        Because the golden was generated from the live index (synthesis ON), every
        expected symbol is reachable — so aggregate recall = 1.0 is the correct bar.
        Any change that causes a query to drop a symbol should surface here AND in
        the corresponding per-query test.
        """
        cases = []
        for query_spec in golden.get("queries", []):
            actual = _run_query(indexed_conn, query_spec)
            cases.append(
                {
                    "expected_symbols": query_spec["expected_symbols"],
                    "actual_symbols": actual,
                }
            )

        metrics = compute_metrics(cases, k=10)
        n = metrics["n"]

        assert metrics["recall_at_k"] >= 1.0 - _RECALL_TOLERANCE, (
            f"Aggregate recall@10 REGRESSION: expected >= 1.0, got {metrics['recall_at_k']:.4f} "
            f"(over {n} queries)"
        )
        assert metrics["mrr"] >= 1.0 - _RECALL_TOLERANCE, (
            f"Aggregate MRR REGRESSION: expected >= 1.0, got {metrics['mrr']:.4f} "
            f"(over {n} queries)"
        )

    def test_synthesis_sensitive_queries_meet_baseline(
        self, indexed_conn: Any, golden: dict[str, Any]
    ) -> None:
        """Synthesis-sensitive queries (EE + interface-override) must all recall 1.0.

        These queries specifically test the delta that slices 1+2 add: without
        synthesis the interface-override and event-emitter queries would return empty
        impact lists. We assert they achieve full recall so any regression in the
        synthesis pass is immediately visible.
        """
        synthesis_ids = {
            "impact-downstream-event-emitter-synthesis",
            "impact-downstream-interface-override-synthesis",
            "impact-downstream-interface-refund-synthesis",
        }

        for query_spec in golden.get("queries", []):
            if query_spec["id"] not in synthesis_ids:
                continue

            actual = _run_query(indexed_conn, query_spec)
            expected = query_spec["expected_symbols"]
            k = query_spec.get("k", 10)

            actual_recall = recall_at_k(expected, actual, k)
            assert actual_recall >= 1.0 - _RECALL_TOLERANCE, (
                f"Synthesis query '{query_spec['id']}' REGRESSION:\n"
                f"  recall@{k} = {actual_recall:.4f} (expected >= 1.0)\n"
                f"  expected: {expected}\n"
                f"  actual: {actual[:k]}\n"
                f"  HINT: This query is synthesis-sensitive. Did edge synthesis get disabled?"
            )
